        self.top_layout.addWidget(self._qtgui_time_sink_x_0_win)
        def _my_function_probe_probe():
            while True:

                val = self.probe_level.level()
                try:
                    self.set_my_function_probe(val)
                except AttributeError:
                    pass
                time.sleep(1.0 / (1))
        _my_function_probe_thread = threading.Thread(target=_my_function_probe_probe)
        _my_function_probe_thread.daemon = True
        _my_function_probe_thread.start()

        self.blocks_throttle_0 = blocks.throttle(gr.sizeof_float*1, samp_rate,True)
        self.blocks_add_xx_0 = blocks.add_vff(1)
        self.analog_sig_source_x_0 = analog.sig_source_f(samp_rate, analog.GR_COS_WAVE, freq, amplitude, 0, 0)
        self.analog_noise_source_x_0 = analog.noise_source_f(analog.GR_GAUSSIAN, .1, 0)


        ##################################################
        # Connections
        ##################################################
        self.connect((self.analog_noise_source_x_0, 0), (self.blocks_add_xx_0, 1))
        self.connect((self.analog_sig_source_x_0, 0), (self.blocks_add_xx_0, 0))
        self.connect((self.blocks_add_xx_0, 0), (self.blocks_throttle_0, 0))
        self.connect((self.blocks_throttle_0, 0), (self.probe_level, 0))
        self.connect((self.blocks_throttle_0, 0), (self.qtgui_time_sink_x_0, 0))


